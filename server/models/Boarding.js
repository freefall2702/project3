const mongoose = require('mongoose');
const Owner = require('./Owner');
const Schema = mongoose.Schema;

const BoardingSchema = new Schema({
    owner: {
        type: Schema.Types.ObjectId,
        ref: "owners",
    },
    name: {
        type: String,
        required: true,
    },
    image: {
        type: String,
        required: true,
    },
    type: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    },
    district: {
        type: String,
        required: true,
    },
    city: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    price: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
    deletedAt: {
        type: Date,
        default: null,
    }
})

module.exports = mongoose.model('boardings', BoardingSchema)